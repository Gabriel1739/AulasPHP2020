      <h1 class="pb-3 mb-4 font-italic border-bottom">
        Sobre a Instituição
      </h1>
  
    <div class="blog-post">
  
        <p>A Polícia Rodoviária Federal, sigla PRF, é um órgão policial ostensivo, organizado e mantido pela União, estruturado em carreira, subordinada ao Ministério da Justiça e Segurança Pública, que tem como principal função garantir a segurança nas rodovias federais e em áreas de interesse da União.</p>
        <p>As competências da PRF são definidas pela Constituição Federal no artigo 144, pela Lei nº 9.503/97 (Código de Trânsito Brasileiro), pelo Decreto nº 1.655, de 3 de outubro de 1995 e pelo seu regimento interno, aprovado pela Portaria Ministerial nº 219, de 27 de fevereiro de 2018.</p>
        <blockquote>
        <p>Sua estrutura conta com uma unidade administrativa central, situada em Brasília, e Unidades Administrativas Regionais, representadas por Superintendências. Além disso, é formada por Subunidades Administrativas e Unidades Operacionais (UOPs).</p>
        </blockquote>                       
      
